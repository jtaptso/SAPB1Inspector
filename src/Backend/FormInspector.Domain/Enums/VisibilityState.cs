namespace FormInspector.Domain.Enums;

/// <summary>
/// Represents the visibility state of an SAP UI item.
/// </summary>
public enum VisibilityState
{
    /// <summary>The item is visible.</summary>
    Visible,

    /// <summary>The item is hidden.</summary>
    Hidden,

    /// <summary>The item is collapsed (pane-dependent).</summary>
    Collapsed
}
