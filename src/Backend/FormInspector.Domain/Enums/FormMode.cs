/// <summary>
/// Represents the mode of an SAP Business One form.
/// </summary>
public enum FormMode
{
    /// <summary>Form is in OK (view) mode.</summary>
    Ok,

    /// <summary>Form is in Add mode.</summary>
    Add,

    /// <summary>Form is in Find mode.</summary>
    Find,

    /// <summary>Form is in Update mode.</summary>
    Update
}
